package org.example.javaprogramming.ex3;

public class LoopExample {

    public static void main(String[] args) {

        for (int i = 0; i < 10; i++) {
            System.out.println("i = " + i);
        }

        int count = 0;
        while (count < 10) {
            System.out.println("count = " + count);
            count++;
        }

    }

}
